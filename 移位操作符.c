#include<stdio.h>

int main()
{
	int a = 2;
	int b = a << 1;
	printf("%d\n", a);
	printf("%d\n", b);//输出结果为4

	//分析：<<为左移操作符，运用原理：将二进制数整体向左移若干位置，其中a=2，且int类型占32bit
	//为00000000 00000000 00000000 00000010 左移一位
	//为00000000 00000000 00000000 00000100 转化为十进制为4 
	//右移操作符同理


	return 0;
}